@pytest.mark.django_db
def test_record_advisory_version_appends_next(make_project, make_user):
    user = make_user(email="alice@example.org")
    project = make_project("eclipse-jetty", team_members=[user])
    advisory = Advisory.objects.create(project=project, summary="v1", created_by=user)

    advisory.summary = "v2"
    advisory.save()
    record_advisory_version(advisory, editor=user)

    versions = list(advisory.versions.order_by("version"))
    assert [v.version for v in versions] == [1, 2]
    assert versions[0].payload["summary"] == "v1"
    assert versions[1].payload["summary"] == "v2"
    assert versions[1].editor == user


